number1 = int(input("Enter a number : "))
number2 = int(input("Enter the another number : "))
xxbb = input("choose what to do : + or - or * or / : ")
if xxbb == "+":
    print(int(number1) + int(number2))
if xxbb == "-":
      print(number1 - number2)
if xxbb == "*":
      print(number1 * number2)
if xxbb == "/":
    print(number1 / number2)
